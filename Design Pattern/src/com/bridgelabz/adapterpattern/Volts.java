package com.bridgelabz.adapterpattern;

/**
 * @author use
 *
 */
public class Volts {

	private int volts;

	/**
	 * @param volts
	 */
	public Volts(int volts) {
		
		this.volts = volts;
	}

	/**
	 * @return the volts
	 */
	public int getVolts() {
		return volts;
	}

	/**
	 * @param volts the volts to set
	 */
	public void setVolts(int volts) {
		this.volts = volts;
	}
	
}
